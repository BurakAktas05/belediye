export function t(key: string, lang: Lang = 'tr', params?: Record<string, string | number>): string {
  let text = translations[lang]?.[key] || translations.tr[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
